# used to represent days of the week in a single integer
# example:
# 2 has a value of 0010 in binary
# so it would be Tuesday
# 3 has a value of 0011 in binary
# so it wold be Monday, Tuesday
#
MONDAY = 0b000_0001
TUESDAY = 0b000_0010
WEDNESDAY = 0b000_0100
THURSDAY = 0b000_1000
FRIDAY = 0b001_0000
SATURDAY = 0b010_0000
SUNDAY = 0b100_0000
